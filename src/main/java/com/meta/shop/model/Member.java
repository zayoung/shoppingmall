package com.meta.shop.model;

public class Member {
	private String mId;
	private String mPw;
	private String mName;
	private String mEmail;
	private String mAddress;
	private String mPhone;
	private String mAdmin;
	private String mDel;
	private String mRgstDate;
	
	
	
	public Member() {
	}
	
	public String getmId() {
		return mId;
	}
	public void setmId(String mId) {
		this.mId = mId;
	}
	public String getmPw() {
		return mPw;
	}
	public void setmPw(String mPw) {
		this.mPw = mPw;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getmEmail() {
		return mEmail;
	}
	public void setmEmail(String mEmail) {
		this.mEmail = mEmail;
	}
	public String getmAddress() {
		return mAddress;
	}
	public void setmAddress(String mAddress) {
		this.mAddress = mAddress;
	}
	public String getmPhone() {
		return mPhone;
	}
	public void setmPhone(String mPhone) {
		this.mPhone = mPhone;
	}
	public String getmAdmin() {
		return mAdmin;
	}
	public void setmAdmin(String mAdmin) {
		this.mAdmin = mAdmin;
	}
	public String getmDel() {
		return mDel;
	}
	public void setmDel(String mDel) {
		this.mDel = mDel;
	}
	public String getmRgstDate() {
		return mRgstDate;
	}
	public void setmRgstDate(String mRgstDate) {
		this.mRgstDate = mRgstDate;
	}

	

}
